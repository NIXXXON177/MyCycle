/// Фаза менструального цикла.
enum CyclePhase {
  menstruation('Менструация', 'Сейчас идут месячные. Отдых и забота о себе — главное.'),
  follicular('Фолликулярная фаза', 'Энергия постепенно возвращается. Хорошее время для новых дел.'),
  ovulation('Овуляция', 'Пик энергии и активности. Возможно повышенное либидо.'),
  luteal('Лютеиновая фаза', 'Возможна повышенная чувствительность. Береги себя.'),
  premenstrual('Предменструальная фаза', 'Возможны перепады настроения. Больше отдыха и тепла.');

  const CyclePhase(this.label, this.description);

  final String label;
  final String description;
}
